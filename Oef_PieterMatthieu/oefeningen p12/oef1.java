public class oef1{

	/* De main methode */
	public static void main(String args[])
	{
		drukaf(100);
	}

	
    /*Deze methode drukt de getallen af
    * in een for lus
    */
	public static void drukaf(int m)
	{
		int a;
		for (a=0;a<m;a++)
		{
			System.out.println(a);
		}
	}
}